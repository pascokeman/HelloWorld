#operators in python
#addition
print(10 + 3)

#substraction
print(10 - 3)

#multiplication
print(10 * 3)

#division with float result
print(10 / 3)

#division with whole number/int result
print(10 // 3)

#(modulus) getting the remainder of the equation
print(10 % 3)

#Exponential i.e raise to power
print(10 ** 3)

x = 10
x = x + 3
print(x)
x += 3          #the same as x = x + 3 i.e argumented assignment operator
print(x)

x -= 3
x *= 3

#operator precedence BODMAS
x = 10 + 3 * 2    #16
x = (10 + 3) * 2   #26
